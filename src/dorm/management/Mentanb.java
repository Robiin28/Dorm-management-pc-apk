     public void showTablemen() {
    try {
        PreparedStatement stmt = con.prepareStatement("SELECT * FROM menmaint", ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
        ResultSet rs = stmt.executeQuery();

        // Create a DefaultTableModel
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.addColumn("ID");
        tableModel.addColumn("Type");
        tableModel.addColumn("Room");
        tableModel.addColumn("Level");
        tableModel.addColumn("Block");

        // Populate the table model manually
        while (rs.next()) {
            Object[] row = {
                rs.getInt("id"),
                rs.getString("type"),
                rs.getString("room"),
                rs.getString("level"),
                rs.getString("block")
            };
            tableModel.addRow(row);
        }

        // Set the table model to the JTable
        jtabmen.setModel(tableModel);

        stmt.close();
    } catch (SQLException e) {
        System.out.println("Error showing table: " + e.getMessage());
    }
}


