      private boolean ismaleBlockAlreadyExists(String block) throws SQLException {
    // Query the database to check if the block already exists
    String query = "SELECT block FROM building_men WHERE block = ? ";
    try (PreparedStatement stmt = con.prepareStatement(query)) {
        stmt.setInt(1, Integer.parseInt(block));
        
        try (ResultSet rs = stmt.executeQuery()) {
            return rs.next();
        }
    }
}
