    private void jaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jaddActionPerformed
        // TODO add your handling code here:
        
       String insert_sql;
try {
    if (!jmale.isSelected() && !jfemale.isSelected()) {
            JOptionPane.showMessageDialog(this, "Please select gender.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
   
    
    if (jmale.isSelected()) {
         String block = jblock.getText();
               if (ismaleBlockAlreadyExists(block)) {
                    JOptionPane.showMessageDialog(this, "Block already exists. Please choose a different block.", "Error", JOptionPane.ERROR_MESSAGE);
                   return;
                   }
              else{
                  insert_sql = "INSERT INTO building_men(block, buildName, buildAddress) VALUES (?, ?, ?)";
                } 
              }
               
    else {
         String block = jblock.getText();
               if (isfemaleBlockAlreadyExists(block)) {
                    JOptionPane.showMessageDialog(this, "Block already exists. Please choose a different block.", "Error", JOptionPane.ERROR_MESSAGE);
                   return;
                   }
              else{
                 insert_sql = "INSERT INTO building_women(block, buildingName, buildingAddress) VALUES (?, ?, ?)";
                } 
    }

    // Validate input fields here if needed

    try (PreparedStatement stmt = con.prepareStatement(insert_sql)) {
        stmt.setInt(1, Integer.parseInt(jblock.getText()));
        stmt.setString(2, jbuild.getText());
        stmt.setString(3, jaddress.getText());
        stmt.executeUpdate();
    }

    JOptionPane.showMessageDialog(this, "Building Added successfully");
     showTablewomen();
     showTablemen(); 
    // Clear input fields
    clearFields();
    // Refresh table and update ID

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error inserting Building: " + e.getMessage());
    e.printStackTrace();
}

    }//GEN-LAST:event_jaddActionPerformed

