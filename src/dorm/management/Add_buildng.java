    private void jdelfemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jdelfemActionPerformed

   try {
        int row = jtabfem.getSelectedRow();
        if (row == -1) {
            // No row selected
            JOptionPane.showMessageDialog(this, "Please select a row to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int block = (int) jtabfem.getValueAt(row, 0);

        PreparedStatement stmt = con.prepareStatement("DELETE FROM building_women WHERE block = ?", ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
        stmt.setInt(1, block);

        stmt.executeUpdate();
        

        JOptionPane.showMessageDialog(this, "building deleted successfully");

        stmt.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error deleting building: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
       
       showTablemen();   
        
        
    }//GEN-LAST:event_jdelfemActionPerformed

